package beans;

public class LanguageType {
	private String name;
	private boolean primitive;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public boolean isPrimitive() {
		return primitive;
	}
	
	public void setPrimitive(boolean primitive) {
		this.primitive = primitive;
	}
}
