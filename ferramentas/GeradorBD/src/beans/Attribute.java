package beans;

public class Attribute {
	private LanguageType type; 
	private String name;
	
	public LanguageType getType() {
		return type;
	}
	
	public void setType(LanguageType type) {
		this.type = type;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}
